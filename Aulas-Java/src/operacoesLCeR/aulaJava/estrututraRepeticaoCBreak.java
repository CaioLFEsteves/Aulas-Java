package operacoesLCeR.aulaJava;

public class estrututraRepeticaoCBreak {

	public static void main(String[] args) {
		
		/*ESTRTUTRA DA REPETIÇÃO FOR COM BREAK*/
		
		
		for (int num1 = 0; num1 <= 10; num1 ++) {
			if(num1 == 7) {
			System.out.println("Número 7 encontrado");
			break; /*Assim que o método é realizado, o programa para a execução.*/
			}
		}
		
		
	}
}
