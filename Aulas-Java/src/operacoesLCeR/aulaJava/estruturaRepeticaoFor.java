package operacoesLCeR.aulaJava;

public class estruturaRepeticaoFor {

	public static void main(String[] args) {
			
			/*ESTRTUTRA DA REPETIÇÃO FOR*/
			
			
			for (int num1 = 0; num1 <= 10; num1 ++) {/*Repete a ação enquanto a condição for true. Lê-se
			"Para variavel igual a zero, enquanto variavel menor  ou igual a 10, variável soma 1 e verifica.*/
				
				System.out.println("O número atual é: " + num1);
			}
			
			/*-------------------------------------------------------------------------*/
			
			for (int num2 = 10; num2 >= 0; num2 --) {/*Repete a ação enquanto a condição for true. Lê-se
				"Para variavel igual a dez, enquanto variavel maior ou igual a 0, variável subtrai 1 e verifica.*/
					
					System.out.println("O número atual é: " + num2);
			}
	}
}
